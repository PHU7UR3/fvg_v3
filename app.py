def bot_loop():
    add_log("🤖 FVG Bot started")
    try:
        api = get_api()
        acc = api.get_account()
        with lock:
            runtime["equity"]=float(acc.equity)
            runtime["cash"]=float(acc.cash)
        add_log(f"💰 Connected: ${float(acc.equity):,.2f}")
    except Exception as e:
        add_log(f"❌ Connection failed: {e}","error")
        with lock: runtime["running"]=False
        return
 
    setup_account()
 
    while True:
        with lock:
            if not runtime["running"]: break
        try:
            # Refresh account
            try:
                api=get_api(); acc=api.get_account()
                with lock:
                    runtime["equity"]=float(acc.equity)
                    runtime["cash"]=float(acc.cash)
            except: pass
 
            # Market check
            open_ = is_market_open()
            with lock: runtime["market_open"]=open_
 
            if not open_:
                add_log(f"💤 Market closed — next: {runtime['next_open']} UTC")
                time.sleep(60); continue
 
            # Get real positions from Alpaca
            api = get_api()
            positions = api.list_positions()
            pos_dict = {p.symbol:p for p in positions}
            with lock:
                runtime["last_scan"]=datetime.now().strftime("%H:%M:%S")
                runtime["positions"]=[{
                    "symbol":p.symbol,"qty":p.qty,
                    "entry":float(p.avg_entry_price),"current":float(p.current_price),
                    "pnl":float(p.unrealized_pl),
                    "pnl_pct":round(float(p.unrealized_plpc)*100,2),
                    "side":"long" if float(p.qty)>0 else "short"
                } for p in positions]
 
            # Monitor and close positions at TP/SL
            monitor_and_close()
            update_pnl()
 
            s=state["settings"]
            equity=runtime["equity"]; cash=runtime["cash"]
            max_pos=s["max_positions"]
 
            add_log(f"📡 {len(state['watchlist'])} stocks | ${equity:,.0f} | {len(pos_dict)}/{max_pos} pos")
 
            if len(pos_dict)>=max_pos:
                add_log(f"⚠️ {max_pos} positions full — monitoring for closes")
                time.sleep(s["check_interval"]); continue
 
            with lock: watchlist=state["watchlist"][:]
 
            for symbol in watchlist:
                with lock:
                    if not runtime["running"]: break
                if symbol in pos_dict: continue
 
                bars=get_candles(symbol)
                if bars is None or len(bars)<10:
                    add_log(f"📊 {symbol}: no candle data")
                    continue
 
                price = float(bars["close"].iloc[-1])
                rsi   = calc_rsi(bars["close"], int(s["rsi_period"]))
                trend = get_trend(symbol) if s["use_ema_trend"] else "neutral"
                fvgs  = detect_fvg(bars)
 
                add_log(f"📊 {symbol} ${price:.2f} RSI={rsi} {trend} FVGs={len(fvgs)}")
                if not fvgs: continue
 
                with lock: state["fvg_count"] += len(fvgs)
                reward  = s["reward_ratio"]
                traded  = False
 
                for fvg in fvgs[:3]:
                    if traded: break
                    ftype = fvg["type"]
                    add_log(f"🎯 {symbol} {ftype} gap={fvg['gap_size']}% top={fvg['top']:.2f} bot={fvg['bottom']:.2f}")
 
                    # BULLISH — buy when price is at or below gap top
                    if ftype == "bullish" and trend in ["bullish","neutral"]:
                        if price <= fvg["top"] * 1.005:
                            if not s["use_rsi"] or rsi < s["rsi_overbought"]:
                                entry = round(price, 2)
                                sl    = round(min(fvg["bottom"] * 0.997, entry * 0.985), 2)
                                tp    = round(entry + (entry - sl) * reward, 2)
                                qty   = calc_qty(equity, cash, entry, sl)
                                if qty > 0:
                                    place_order(symbol,"buy",qty,entry,sl,tp,fvg,trend,rsi)
                                    traded = True
                                else:
                                    add_log(f"⚠️ {symbol} qty=0 cash=${cash:.0f} entry=${entry}")
                            else:
                                add_log(f"⏭️ {symbol} RSI={rsi} overbought skip")
                        else:
                            add_log(f"⏭️ {symbol} price ${price:.2f} > gap top ${fvg['top']:.2f}")
 
                    # BEARISH — sell when price is at or above gap bottom
                    elif ftype == "bearish" and trend in ["bearish","neutral"]:
                        if price >= fvg["bottom"] * 0.995:
                            if not s["use_rsi"] or rsi > s["rsi_oversold"]:
                                entry = round(price, 2)
                                sl    = round(max(fvg["top"] * 1.003, entry * 1.015), 2)
                                tp    = round(entry - (sl - entry) * reward, 2)
                                qty   = calc_qty(equity, cash, entry, sl)
                                if qty > 0:
                                    place_order(symbol,"sell",qty,entry,sl,tp,fvg,trend,rsi)
                                    traded = True
                                else:
                                    add_log(f"⚠️ {symbol} qty=0 cash=${cash:.0f} entry=${entry}")
                            else:
                                add_log(f"⏭️ {symbol} RSI={rsi} oversold skip")
                        else:
                            add_log(f"⏭️ {symbol} price ${price:.2f} < gap bot ${fvg['bottom']:.2f}")
                    else:
                        add_log(f"⏭️ {symbol} trend mismatch: fvg={ftype} trend={trend}")
            save_state()
            time.sleep(s["check_interval"])
 
        except Exception as e:
            add_log(f"❌ Error: {e}","error")
            time.sleep(30)
 
    add_log("⏹ Stopped")
 
def watchdog():
    time.sleep(20)
    while True:
        time.sleep(30)
        with lock: running=runtime["running"]
        if running:
            alive=any(t.name=="bot" and t.is_alive() for t in threading.enumerate())
            if not alive:
                add_log("🔄 Watchdog restarting bot","error")
                threading.Thread(target=bot_loop,daemon=True,name="bot").start()
 
def start_bot():
    with lock:
        if runtime["running"]: return
        runtime["running"]=True
    threading.Thread(target=bot_loop,daemon=True,name="bot").start()
 
